// trim <- 공백 자르기
console.log("                 hello");
console.log("                 hello".trimStart());
console.log("hello                 ");
console.log("hello                 ".trimEnd());
console.log("                 hello                ");
console.log("                 hello                ".trim());
// 단어 사이 공백 제거 불가
console.log("                 hel lo                ".trim());